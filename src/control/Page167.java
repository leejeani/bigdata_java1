package control;

public class Page167 {

	public static void main(String[] args) {
		// for TEST -------------------------
		for(int i=1;i<=3;i++){
			System.out.println("For TEST"+i);
		}
		
		// while 1 TEST -----------------------
		int s = 1;
		while(s<=3){
			System.out.println("While TEST"+s);
			s++;
		}
		
		// while 1 TEST -----------------------
		int x = 1;
		do{
			System.out.println("While TEST"+x);
			x++;
		}while(x<=3);
		System.out.println(x);
		
	}

}






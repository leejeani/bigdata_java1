package variable;

public class Page32 {

	public static void main(String[] args) {
		// 8진수
		int octNum = 010;
		// 16진수
		int hexNum = 0x61;
		// 2진수
		int binNum = 0b10;
		float fNum = 3.14e-3F;
		
		System.out.println(octNum);
		System.out.println(hexNum);
		System.out.println(binNum);
		System.out.println(fNum);

		long i = 0x123456789L;
		System.out.println(i);
	}

}





